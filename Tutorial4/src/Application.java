
public class Application {
	/*
	 * Writing "main" + ctrl + esp it will create the main method.
	 * Also with ctrl + shift + F, it will format the code (e.g. indent) 
	 */
	
	public static void main(String[] args) {
		int value = 0;
		
		while (value < 20) {
			System.out.println("Hello " + value);
			value = value + 1;
		}
	}
}
 